#int -> Número inteiro. INT representa qualquer número.
print(10)
print(-11)
print(0)


# float -> Números com ponto flutuante (números decimais)
print(1.1)
print(-11.2)

#type() -> Mostra o tipo de dado
print(type(10))  # <class 'int'>
print(type(1.1))  # <class 'float'>

# str -> String (texto)
print('Luiz')
print(type('Luiz'))  # <class 'str'>

# bool -> Booleano (True ou False)
print(True)
print(False)
print(type(True))  # <class 'bool'>
print(type(False))  # <class 'bool'>
