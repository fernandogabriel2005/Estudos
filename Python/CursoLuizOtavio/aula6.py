

#convertendo tipos de dados

# int -> float
print(int(1.1))  # 1

# float -> int
print(float(1))  # 1.0

# int -> str
print(str(11)+ 'b')  # '11'

# str -> int
print(int('11'))  # 11

# str -> float
print(float('11.1'))  # 11.1

# float -> str
print(str(11.1))  # '11.1'

# bool -> int
print(int(True))  # 1

# int -> bool
print(bool(1))  # True

# bool -> str
print(str(True))  # 'True'

# str -> bool
print(bool(''))  # False (string vazia Ã© False)

