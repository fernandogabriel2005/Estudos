#operações aritméticas
print(10 + 10)  # Adição
print(10 - 5)   # Subtração
print(10 * 2)   # Multiplicação
print(10 / 2)   # Divisão
print(10 // 3)  # Divisão inteira
print(10 % 3)   # Módulo (resto da divisão)
print(10 ** 2)  # Exponenciação (potência)
# Operações com variáveis
x = 10
y = 5
print(x + y)  # Adição
print(x - y)  # Subtração
print(x * y)  # Multiplicação
print(x / y)  # Divisão
print(x // y)  # Divisão inteira
print(x % y)   # Módulo (resto da divisão)
print(x ** y)  # Exponenciação (potência)
# Operações com strings
nome = 'Fernando'
sobrenome = 'Gabriel'
print(nome + ' ' + sobrenome)  # Concatenação de strings
print(nome * 3)  # Repetição de string
